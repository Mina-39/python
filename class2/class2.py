# 運算子
print(1 + 2)  # 加法
print(5 - 3)  # 減法
print(4 * 2)  # 乘法
print(8 / 4)  # 除法
print(7 // 3)  # 取整數
print(7 % 3)  # 取餘數
print(2**3)  # 次方
# 優先順序
# 1. 括號 ()
# 2. 次方 **
# 3. + - 正負號
# 4. * / // % 乘 除 取整數 取餘數
# 5. + - 加 減
print(3 + 4 * 2)  # 11
print((3 + 4) * 2)  # 14
print(2**3**2)  # 512，次方是由右往左算
# 字串運算
print("Hello " + "World")  # 字串串接
print("Hi " * 3)  # 字串重複
