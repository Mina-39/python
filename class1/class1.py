# 基本型態
print("Hello World")  # 字串 str
print(123)  # 整數 int
print(12.34)  # 浮點數 float
print(True)  # 布林值 True/False bool
print(None)  # 空值 None
# 變數命名規則：只能包含字母、數字、底線，且不能以數字開頭，區分大小寫
hi = "Hello World"  # 把字串 "Hello World" 存到變數 hi 裡面
# = 的功能是把右邊的東西存到左邊的變數中
print(hi)  # 印出變數 hi 裡面的東西
